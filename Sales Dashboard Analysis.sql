ROUND(SUM(o.unit_price * o.quantity * (1 - o.discount/100))/count(distinct e.employee_id), 2) AS avg_revenu 
FROM orders o INNER JOIN employees e ON o.employee_id = e.employee_id
WHERE o.order_status = 'Completed'
GROUP BY e.department, MONTHNAME(o.order_date);
SELECT * FROM employee_performance_by_department;

