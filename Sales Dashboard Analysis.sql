-- Employees With Salary difference from Average salary, below, above and equal to avg salary
-- by each department...
SELECT employee_name, department, salary,
AVG(salary) OVER(PARTITION BY department) AS avg_salary,
salary - AVG(salary) OVER(PARTITION BY department) AS salary_diff,
CASE
	WHEN salary > AVG(salary) OVER(PARTITION BY department) THEN 'YES' ELSE 'NO' 
	END AS above_avg_salary,
CASE
	WHEN salary < AVG(salary) OVER(PARTITION BY department) THEN 'YES' ELSE 'NO'
    END AS below_avg_salary,
CASE
	WHEN salary = AVG(salary) OVER(PARTITION BY department) THEN 'YES' ELSE 'NO'
    END AS equal_avg_salary
FROM employees;
