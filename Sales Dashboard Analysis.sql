-- 1. CUSTOMER-WISE TOTAL ORDERS & QUANTITY ANALYSIS
-- Total orders placed and total quantity purchased 
-- by each customer for completed transactions


SELECT 
    c.user_name as customer_name,
    SUM(o.quantity) as total_quantity,
    COUNT(o.order_id) as total_orders
FROM orders o
INNER JOIN customers c ON o.customer_id = c.customer_id
WHERE o.order_status = 'Completed'
GROUP BY c.customer_id, c.user_name
ORDER BY total_orders DESC;

-- 2. PRODUCT-WISE REVENUE PERFORMANCE (AFTER DISCOUNT)
-- Ranking products based on total revenue generated, 
-- with discount adjustments applied

SELECT 
    p.product_name,
    SUM(o.unit_price * o.quantity * (1 - o.discount/100)) as total_revenue
FROM orders o
INNER JOIN products p ON o.product_id = p.product_id
WHERE o.order_status = 'Completed'
GROUP BY p.product_id, p.product_name
ORDER BY total_revenue DESC;

-- 3. 30-DAY INACTIVE CUSTOMER ANALYSIS
-- Identifying customers with no orders in the last 30 days, 
-- showing last order date and days since last purchase

SELECT 
    c.user_name,
    c.email,
    MAX(o.order_date) as last_order_date,
    DATEDIFF(CURDATE(), MAX(o.order_date)) AS days_since_order
FROM orders o
INNER JOIN customers c ON o.customer_id = c.customer_id
GROUP BY c.customer_id, c.user_name, c.email
HAVING days_since_order > 30
ORDER BY days_since_order DESC;

-- 4. EMPLOYEE MONTHLY PERFORMANCE DASHBOARD
-- Monthly breakdown of orders handled and revenue 
-- generated by each employee

SELECT 
    e.employee_name,
    MONTHNAME(o.order_date) as month_name,
    COUNT(o.order_id) as total_orders,
    SUM(o.quantity * o.unit_price * (1 - o.discount/100)) as total_revenue
FROM orders o
INNER JOIN employees e ON o.employee_id = e.employee_id
WHERE o.order_status = 'Completed'
GROUP BY e.employee_id, e.employee_name, MONTHNAME(o.order_date), MONTH(o.order_date)
ORDER BY MONTH(o.order_date), e.employee_name;

-- 5. CATEGORY-WISE AVERAGE ORDER VALUE (AOV) ANALYSIS
-- Calculating Average Order Value (AOV) per product category
-- AOV = Total Revenue / Total Number of Orders


SELECT 
    p.category,
    COUNT(o.order_id) as total_orders,
    SUM(o.quantity * o.unit_price * (1 - o.discount/100)) as total_revenue,
    ROUND(
        SUM(o.quantity * o.unit_price * (1 - o.discount/100)) / 
        COUNT(o.order_id), 
        2
    ) as AOV
FROM orders o
INNER JOIN products p ON o.product_id = p.product_id
WHERE o.order_status = 'Completed'
GROUP BY p.category
ORDER BY AOV DESC;
-- View of employees perfomence monthly by department
CREATE VIEW employee_performance_by_department AS
SELECT e.department, MONTHNAME(o.order_date) AS month_name, 
COUNT(distinct e.employee_id) AS total_employees,
COUNT(o.order_id) AS total_orders, 
